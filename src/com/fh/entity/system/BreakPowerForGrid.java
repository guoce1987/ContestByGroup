package com.fh.entity.system;

public class BreakPowerForGrid {
	private String id;
	private String breakDate;
	private String dutyClass;
	private String dutyName;
	private String groupName;
	private String breakPower;
	private String createDate;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBreakDate() {
		return breakDate;
	}
	public void setBreakDate(String breakDate) {
		this.breakDate = breakDate;
	}
	public String getDutyClass() {
		return dutyClass;
	}
	public void setDutyClass(String dutyClass) {
		this.dutyClass = dutyClass;
	}
	public String getBreakPower() {
		return breakPower;
	}
	public void setBreakPower(String breakPower) {
		this.breakPower = breakPower;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getDutyName() {
		return dutyName;
	}
	public void setDutyName(String dutyName) {
		this.dutyName = dutyName;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	
}
